calc :: Double
calc = let mark = [(2.0,4.1),(3.0,3.5),(2.5,3.2),(2.0,3.8),(2.0,3.8),(4.0,2.8),(2.5,3.5),(0.5,3.7),(1.0,4.1),(0.5,4.5),(6.0,3.0),(1.0,4.3)]
       in (sum $ map (\(x, y) -> x*y) mark) / (sum $ map (\(x, _) -> x) mark)

